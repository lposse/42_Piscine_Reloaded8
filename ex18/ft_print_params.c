#include <unistd.h>

void	ft_putchar(char c);

void	ft_print_params(char *a)
{
	while (*a != '\0')
	{
		ft_putchar(*a);
		a++;
	}
	ft_putchar('\n');
}

int	main(int argc, char *argv[])
{
	int	i;

	i = 1;
	while (i < argc)
	{
		ft_print_params(argv[i]);
		i++;
	}
	return (0);
}
