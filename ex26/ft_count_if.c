#include <unistd.h>

int	ft_count_if(char **tab, int (*f)(char *))
{
	int	i;
	int	count;

	count = 0;
	i = 0;
	while (tab[i] != NULL)
	{
		if (f(tab[i]) == 1)
		{
			count++;
		}
		i++;
	}
	return (count);
}
